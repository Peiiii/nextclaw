import { existsSync, mkdirSync, readdirSync, readFileSync, rmSync, statSync, writeFileSync } from "node:fs";
import { basename, dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(scriptDir, "..");
const source = resolve(pkgRoot, "..", "..", "docs", "USAGE.md");
const target = resolve(pkgRoot, "resources", "USAGE.md");
const usageDetailsSource = resolve(pkgRoot, "..", "..", "docs", "usage");
const usageDetailsTarget = resolve(pkgRoot, "resources", "usage");

if (!existsSync(source)) {
  console.warn(`[sync-usage-resource] source not found: ${source}`);
  process.exit(0);
}

const sourceContent = readFileSync(source, "utf-8");
const generatedHeader = [
  "<!-- Generated by packages/nextclaw/scripts/sync-usage-resource.mjs -->",
  "<!-- Do not edit this file directly; edit docs/USAGE.md instead. -->",
  ""
].join("\n");

function syncMarkdownFile(sourcePath, targetPath, sourceLabel) {
  const content = readFileSync(sourcePath, "utf-8");
  const header = [
    "<!-- Generated by packages/nextclaw/scripts/sync-usage-resource.mjs -->",
    `<!-- Do not edit this file directly; edit ${sourceLabel} instead. -->`,
    ""
  ].join("\n");
  mkdirSync(dirname(targetPath), { recursive: true });
  writeFileSync(targetPath, `${header}${content}`);
}

function syncUsageDetails() {
  if (!existsSync(usageDetailsSource)) {
    return;
  }
  rmSync(usageDetailsTarget, { force: true, recursive: true });
  for (const entry of readdirSync(usageDetailsSource)) {
    const sourcePath = join(usageDetailsSource, entry);
    if (!statSync(sourcePath).isFile() || !entry.endsWith(".md")) {
      continue;
    }
    syncMarkdownFile(
      sourcePath,
      join(usageDetailsTarget, entry),
      `docs/usage/${basename(sourcePath)}`
    );
  }
}

mkdirSync(dirname(target), { recursive: true });
writeFileSync(target, `${generatedHeader}${sourceContent}`);
syncUsageDetails();
console.log(`[sync-usage-resource] synced ${source} -> ${target}`);
